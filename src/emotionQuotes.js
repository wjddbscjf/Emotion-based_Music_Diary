/**
 * mood에 맞는 감성 문구 하나를 랜덤으로 반환
 */
export function pickEmotionQuote(mood) {
  const list = QUOTES[mood];
  if (!list || list.length === 0) return null;

  const idx = Math.floor(Math.random() * list.length);
  return list[idx];
}
